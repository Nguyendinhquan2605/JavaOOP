package getterAndsetter;

public class Toado {
    private int x ;
    private int y ;

    //getter cua thuoc tinh x
    public int getX()
    {
        return x ;
    }

    //setter cua thuoc tinh x
    public void setX(int x)
    {
        this.x = x ;
    }

    //getter cua thuoc tinh y
    public int getY()
    {
        return y ;
    }

    //setter cua thuoc tinh y
    public void setY()
    {
        this.y = y ;
    }
}
